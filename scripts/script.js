function generarFiltros() {
    const todasLasCategorias = cuentos.flatMap(c => c.categorias || []);
    const categoriasUnicas = ["todos", ...new Set(todasLasCategorias)];

    menuCategorias.innerHTML = "";
    
    categoriasUnicas.forEach(cat => {
        const boton = document.createElement('button');
        boton.className = 'btn-filtro';
        if (cat === "todos") boton.classList.add('active');
        boton.textContent = cat;
        boton.dataset.categoria = cat;

        boton.addEventListener('click', () => {
            const btnActivoAnterior = document.querySelector('.btn-filtro.active');
            if (btnActivoAnterior) btnActivoAnterior.classList.remove('active');
            boton.classList.add('active');
            renderCuentos(cat);
        });

        menuCategorias.appendChild(boton);
    });
}

// 2. MOSTRAR TARJETAS GRÁFICAS
function renderCuentos(filtro = "todos") {
    contenedor.innerHTML = "";
    
    const listaFiltrada = filtro === "todos" 
        ? cuentos 
        : cuentos.filter(c => c.categorias && c.categorias.includes(filtro));

    listaFiltrada.forEach(cuento => {
        // Crear etiquetas
        const etiquetasHTML = (cuento.categorias || []).map(cat => 
            `<span class="categoria-tag">${cat}</span>`
        ).join("");

        // Usar portada por defecto si no tiene
        const portadaUrl = cuento.portada || 'https://via.placeholder.com/300x250.png?text=Cuento';

        const card = document.createElement('div');
        card.className = 'tarjeta-cuento';
        // Hacemos que toda la tarjeta sea clickable
        card.setAttribute('onclick', `abrirCuento(${cuento.id})`);

       // Dentro de renderCuentos
card.innerHTML = `
    <div class="portada-contenedor" style="background-image: url('${portadaUrl}')">
        <div class="tags-contenedor">${etiquetasHTML}</div>
        
        <div class="btn-play-flotante">
            <i class="fas fa-play"></i>
        </div>
    </div>
    
    <div class="info-cuento">
        <h3>${cuento.titulo}</h3>
        <p>${cuento.resumen}</p>
    </div>
`;
        contenedor.appendChild(card);
    });
}

